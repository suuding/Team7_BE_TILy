CREATE SCHEMA IF NOT EXISTS `tily` DEFAULT CHARACTER SET utf8mb4;

USE `tily`;


CREATE TABLE alarm_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        created_date timestamp,
        updated_date timestamp,
        is_read boolean,
        comment_id bigint,
        receiver_id bigint,
        til_id bigint,
);

create table comment_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        created_date timestamp DEFAULT CURRENT_TIMESTAMP,
        updated_date timestamp DEFAULT CURRENT_TIMESTAMP,
        content TEXT,
        is_deleted boolean,
        til_id bigint,
        writer_id bigint,
);

create table image (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        image_path varchar(255),
        original_image_name varchar(255),
        storage_image_name varchar(255),
);

create table reference_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        category varchar(255) not null,
        is_deleted boolean,
        link TEXT,
        step_id bigint,
);

create table roadmap_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        created_date timestamp,
        updated_date timestamp,
        category varchar(255) not null,
        code varchar(255),
        current_num bigint,
        description TEXT,
        image varchar(255),
        is_deleted boolean,
        is_public boolean,
        is_recruit boolean,
        name varchar(255) not null,
        step_num integer,
        creator_id bigint,
);

CREATE TABLE step_tb (
       id bigint generated by default as identity,
        description TEXT,
        due_date timestamp,
        is_deleted boolean,
        title varchar(255) not null,
        roadmap_id bigint,
);

CREATE TABLE til_tb (
       id bigint generated by default as identity,
        created_date timestamp DEFAULT CURRENT_TIMESTAMP,
        updated_date timestamp DEFAULT CURRENT_TIMESTAMP,
        comment_num integer,
        content TEXT,
        is_deleted boolean,
        is_personal boolean,
        submit_content TEXT,
        submit_date timestamp,
        title varchar(255) not null,
        roadmap_id bigint,
        step_id bigint,
        writer_id bigint,
);

CREATE TABLE user_roadmap_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        created_date timestamp DEFAULT CURRENT_TIMESTAMP,
        updated_date timestamp DEFAULT CURRENT_TIMESTAMP,
        content TEXT,
        is_accept boolean,
        is_deleted boolean,
        progress integer not null,
        role varchar(255) not null,
        roadmap_id bigint,
        user_id bigint,
);

CREATE TABLE user_step_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        is_deleted boolean,
        is_submit boolean not null,
        roadmap_id bigint,
        step_id bigint,
        user_id bigint,
);

CREATE TABLE user_tb (
       id bigint AUTO_INCREMENT PRIMARY KEY,
        created_date timestamp DEFAULT CURRENT_TIMESTAMP,
        updated_date timestamp DEFAULT CURRENT_TIMESTAMP,
        email varchar(50) not null,
        image varchar(255),
        is_deleted boolean,
        name varchar(50) not null,
        password varchar(100) not null,
        role varchar(255),
);

